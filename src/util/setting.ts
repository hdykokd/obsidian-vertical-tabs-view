import { Setting } from 'obsidian';

const createToggle = (
  containerEl: HTMLElement,
  name: string,
  desc: string,
  initialValue: boolean,
  onChange: (value: boolean) => void,
) => {
  return new Setting(containerEl)
    .setName(name)
    .setDesc(desc)
    .addToggle((toggle) => toggle.setValue(initialValue).onChange(onChange));
};
